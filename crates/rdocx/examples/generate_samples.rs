// Helper: create a minimal valid PNG (solid color with slight gradient)
// ─────────────────────────────────────────────────────────────────────────────
fn create_sample_png(width: u32, height: u32, base_color: [u8; 3]) -> Vec<u8> {
    let mut pixels = Vec::with_capacity((width * height * 4) as usize);
    for y in 0..height {
        for x in 0..width {
            let fy = y as f64 / height as f64;
            let fx = x as f64 / width as f64;
            let r = (base_color[0] as f64 + (1.0 - fy) * 40.0).min(255.0) as u8;
            let g = (base_color[1] as f64 + fx * 30.0).min(255.0) as u8;
            let b = (base_color[2] as f64 + fy * 60.0).min(255.0) as u8;
            pixels.extend_from_slice(&[r, g, b, 255]);
        }
    }

    let mut png_data = Vec::new();
    {
        use std::io::Write;

        // PNG Signature
        png_data.write_all(&[137, 80, 78, 71, 13, 10, 26, 10]).unwrap();

        // IHDR chunk
        let mut ihdr = Vec::new();
        ihdr.extend_from_slice(&width.to_be_bytes());
        ihdr.extend_from_slice(&height.to_be_bytes());
        ihdr.push(8); // bit depth
        ihdr.push(6); // color type: RGBA
        ihdr.push(0); // compression
        ihdr.push(0); // filter
        ihdr.push(0); // interlace
        write_png_chunk(&mut png_data, b"IHDR", &ihdr);

        // IDAT chunk
        let mut raw_data = Vec::new();
        for y in 0..height {
            raw_data.push(0); // filter: None
            let row_start = (y * width * 4) as usize;
            let row_end = row_start + (width * 4) as usize;
            raw_data.extend_from_slice(&pixels[row_start..row_end]);
        }

        let compressed = zlib_store(&raw_data);
        write_png_chunk(&mut png_data, b"IDAT", &compressed);

        // IEND chunk
        write_png_chunk(&mut png_data, b"IEND", &[]);
    }

    png_data
}

fn write_png_chunk(out: &mut Vec<u8>, chunk_type: &[u8; 4], data: &[u8]) {
    use std::io::Write;
    let len = data.len() as u32;
    out.write_all(&len.to_be_bytes()).unwrap();
    out.write_all(chunk_type).unwrap();
    out.write_all(data).unwrap();
    let crc = crc32(chunk_type, data);
    out.write_all(&crc.to_be_bytes()).unwrap();
}

fn crc32(chunk_type: &[u8], data: &[u8]) -> u32 {
    static CRC_TABLE: std::sync::LazyLock<[u32; 256]> = std::sync::LazyLock::new(|| {
        let mut table = [0u32; 256];
        for n in 0..256u32 {
            let mut c = n;
            for _ in 0..8 {
                if c & 1 != 0 {
                    c = 0xEDB88320 ^ (c >> 1);
                } else {
                    c >>= 1;
                }
            }
            table[n as usize] = c;
        }
        table
    });

    let mut crc = 0xFFFFFFFF_u32;
    for &byte in chunk_type.iter().chain(data.iter()) {
        let index = ((crc ^ byte as u32) & 0xFF) as usize;
        crc = CRC_TABLE[index] ^ (crc >> 8);
    }
    crc ^ 0xFFFFFFFF
}

fn zlib_store(data: &[u8]) -> Vec<u8> {
    let mut out = Vec::new();
    out.push(0x78);
    out.push(0x01);

    let chunks: Vec<&[u8]> = data.chunks(65535).collect();
    for (i, chunk) in chunks.iter().enumerate() {
        let is_last = i == chunks.len() - 1;
        out.push(if is_last { 0x01 } else { 0x00 });
        let len = chunk.len() as u16;
        let nlen = !len;
        out.extend_from_slice(&len.to_le_bytes());
        out.extend_from_slice(&nlen.to_le_bytes());
        out.extend_from_slice(chunk);
    }

    let adler = adler32(data);
    out.extend_from_slice(&adler.to_be_bytes());
    out
}

fn adler32(data: &[u8]) -> u32 {
    let mut a: u32 = 1;
    let mut b: u32 = 0;
    for &byte in data {
        a = (a + byte as u32) % 65521;
        b = (b + a) % 65521;
    }
    (b << 16) | a
}
